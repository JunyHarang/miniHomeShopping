package shopping.common.model;

public class Combo05 {
	private String id;		// 사람 이름
	private int cnt;		// 상품 이름
	
		public Combo05() {
			
		}
	

		public String getId() {
			return id;
		}


		public void setId(String id) {
			this.id = id;
		}


		public int getCnt() {
			return cnt;
		}


		public void setCnt(int cnt) {
			this.cnt = cnt;
		}


		@Override
		public String toString() {
			return "Combo05 [id=" + id + ", cnt=" + cnt + "]";
		}

	
} // Class 끝
